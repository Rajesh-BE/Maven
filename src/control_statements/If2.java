package control_statements;

public class If2 {
	public static void main(String[] args) {
		String programming_language="Java";
	if (programming_language!="Java"){
		System.out.println("Java is not one of the best programming language");
}
	else if (programming_language=="Java") {
		System.out.println("Java is one of the best programming language");
}
	else {
		System.out.println("Java is not a programming language");
	}
	}

}
